def iter_action_specs() -> list[dict[str, Any]]:
    specs: list[dict[str, Any]] = []
    for domain in ACTION_DOMAIN_ORDER:
        specs.extend(dict(item) for item in ACTION_SPECS_BY_DOMAIN[domain])
    return sorted(specs, key=lambda item: int(item["order"]))


def action_resource_locks_by_id() -> dict[str, tuple[dict[str, str], ...]]:
    result: dict[str, tuple[dict[str, str], ...]] = {}
    for domain in ACTION_DOMAIN_ORDER:
        for action_id, locks in ACTION_RESOURCE_LOCKS_BY_DOMAIN[domain].items():
            result[action_id] = tuple({"resource": str(item["resource"]), "mode": str(item["mode"])} for item in locks)
    return result
